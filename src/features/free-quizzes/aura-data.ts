export function calculateAuraResult(scoreA: number, scoreB: number): string {
  if (scoreA >= 46) {
    if (scoreB <= 33) return 'gold';
    if (scoreB >= 34 && scoreB <= 45) return 'red';
    if (scoreB >= 46 && scoreB <= 57) return 'orange';
    if (scoreB >= 58) return 'pink';
  } else {
    if (scoreB <= 33) return 'indigo';
    if (scoreB >= 34 && scoreB <= 45) return 'green';
    if (scoreB >= 46 && scoreB <= 57) return 'purple';
    if (scoreB >= 58) return 'silver';
  }
  // Fallback, shouldn't happen with correct scores
  return 'gold';
}
